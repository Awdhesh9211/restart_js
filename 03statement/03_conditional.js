// if statement
let a = 5; // Declaration and assignment
if (a > 0) {
    console.log("Positive number");
  }
  
  // if-else statement
  if (a > 0) {
    console.log("Positive");
  } else {
    console.log("Negative or zero");
  }
  
  // if-else if-else statement
  if (a > 0) {
    console.log("Positive");
  } else if (a < 0) {
    console.log("Negative");
  } else {
    console.log("Zero");
  }
  
  // switch statement
  let fruit = "apple";
  switch (fruit) {
    case "apple":
      console.log("Apple selected");
      break;
    case "banana":
      console.log("Banana selected");
      break;
    default:
      console.log("No fruit selected");
  }
  